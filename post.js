function posts() {
  if (!postSection) {
    console.error("Post section not found!");
    return;
  }

  postDets.forEach((value) => {
    const post = document.createElement("div");
    post.classList.add("post"); // Add a class for each post container

    post.innerHTML = `
        <div class="info">
            <div class="proInfo">
                <img src="${value.proPic}" alt="">
                <span class="idName">${value.name}</span>
            </div>
            <div class="tripleDot">
                <i class="ri-more-2-fill"></i>
            </div>
        </div>

        <!-- Post Image -->

        <div class="container">
            <img class="mainImg" src="${value.postImg || ""}" alt="${
      value.caption || ""
    }">
          <!--   <i class="ri-heart-3-fill dblHeart"></i>  -->
        </div>

        <!-- Post Interaction Buttons -->

        <div class="btn-cont">
            <div class="leftBtn">
                <i class="ri-heart-3-fill heart"></i>
                <span class="likeCount">${value.like || 0}</span>
                <i class="ri-chat-3-line"></i>
                <span class="commentCount">${value.comment || 0}</span>
                <i class="ri-send-plane-line"></i>
                <span class="shareCount">${value.share || 0}</span>
            </div>
            <div class="rightBtn">
                <i class="ri-bookmark-line"></i>
            </div>
        </div>

        <!-- Post Detail -->

        <p>${value.caption || ""}
        </p>
        <h6>${value.postTime || ""}</h6>
    `;

    postSection.appendChild(post); // Append the post to the postSection
  });
}

